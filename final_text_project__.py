import re
import warnings
import joblib
import nltk
import numpy as np
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

from nltk.corpus import stopwords
from nltk.stem import WordNetLemmatizer
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression
from sklearn.naive_bayes import MultinomialNB
from sklearn.svm import LinearSVC
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, f1_score, classification_report
from sklearn.utils.class_weight import compute_sample_weight
from xgboost import XGBClassifier

warnings.filterwarnings("ignore")

"""#### Problem Statement
The aim of this project is to classify research paper text into the correct category such as `cs.CV`, `cs.CL`, `cs.LG` and other labels using machine learning models.
"""

nltk.download('stopwords')
nltk.download('wordnet')
nltk.download('omw-1.4')

# Load the dataset
# Change the file path if your dataset is in a different location

df = pd.read_csv(r"/content/arxiv_top10_labels.csv", engine='python', on_bad_lines='warn')

df.head()

"""## Data Cleaning"""

df.info()

df.isnull().sum()

df.duplicated().sum()

# Remove duplicated headers inside data
if 'abstract' in df.columns:
    df = df[df['abstract'] != 'abstract']
if 'label' in df.columns:
    df = df[df['label'] != 'label']

df

# Clean label column a little

df['label'] = df['label'].astype(str).str.strip()
df['label'] = df['label'].apply(lambda x: x.split()[0])

# Keep only top 10 labels
# This makes the project easier to manage and compare

top_labels = df['label'].value_counts().nlargest(10).index
df = df[df['label'].isin(top_labels)].reset_index(drop=True)

print(df['label'].value_counts())

plt.figure(figsize=(10,5))
df['label'].value_counts().plot(kind='bar', color='steelblue')
plt.title('Label Distribution')
plt.xlabel('Class')
plt.ylabel('Count')
plt.xticks(rotation=45)
plt.show()

# Check text length

df['abstract_length'] = df['abstract'].astype(str).apply(lambda x: len(x.split()))

plt.figure(figsize=(6,4))
sns.boxplot(y=df['abstract_length'])
plt.title('Abstract Length Distribution')
plt.ylabel('Number of Words')
plt.show()

"""## Preprocessing"""

stop_words = set(stopwords.words('english'))
lemmatizer = WordNetLemmatizer()

def clean_text(text):
    text = str(text).lower()
    text = re.sub(r'[^a-zA-Z\s]', ' ', text)
    text = re.sub(r'\s+', ' ', text).strip()

    words = text.split()
    words = [word for word in words if word not in stop_words]
    words = [lemmatizer.lemmatize(word) for word in words]

    return ' '.join(words)

df['clean_abstract'] = df['abstract'].apply(clean_text)

df[['abstract', 'clean_abstract']].head()

# Remove duplicate abstracts
# This keeps only unique text records

df = df.drop_duplicates(subset=['clean_abstract'])
df = df.reset_index(drop=True)

# Removing empty rows after preprocessing

df = df[df['clean_abstract'].str.strip() != ''].reset_index(drop=True)
print(df.shape)

"""### Defining Target Variable"""

X = df['clean_abstract']
y = df['label']

label_encoder = LabelEncoder()
y_encoded = label_encoder.fit_transform(y)

print('Encoded classes:')
for i, label_name in enumerate(label_encoder.classes_):
    print(i, '->', label_name)

"""## Train Test Split"""

X_train, X_test, y_train, y_test = train_test_split(
    X,
    y_encoded,
    test_size=0.2,
    random_state=42,
    stratify=y_encoded
)

print('X_train shape :', X_train.shape)
print('X_test shape  :', X_test.shape)
print('y_train shape :', y_train.shape)
print('y_test shape  :', y_test.shape)

"""## TF-IDF Feature Extraction"""

tfidf = TfidfVectorizer(
    max_features=20000,
    ngram_range=(1, 2),
    min_df=2,
    max_df=0.95
)

X_train_tfidf = tfidf.fit_transform(X_train)
X_test_tfidf = tfidf.transform(X_test)

print('TF-IDF train shape:', X_train_tfidf.shape)
print('TF-IDF test shape :', X_test_tfidf.shape)

"""## Model Training"""

results = []
trained_models = {}

sample_weights = compute_sample_weight(class_weight='balanced', y=y_train)

# Logistic Regression
log_model = LogisticRegression(max_iter=1000, class_weight='balanced', random_state=42)
log_model.fit(X_train_tfidf, y_train)

y_pred_log = log_model.predict(X_test_tfidf)
acc_log = accuracy_score(y_test, y_pred_log)
f1_log = f1_score(y_test, y_pred_log, average='macro')

print('Logistic Regression Accuracy   :', round(acc_log, 4))
print('Logistic Regression Macro F1   :', round(f1_log, 4))

results.append(['Logistic Regression', acc_log, f1_log])
trained_models['Logistic Regression'] = log_model

# Multinomial Naive Bayes
nb_model = MultinomialNB()
nb_model.fit(X_train_tfidf, y_train)

y_pred_nb = nb_model.predict(X_test_tfidf)
acc_nb = accuracy_score(y_test, y_pred_nb)
f1_nb = f1_score(y_test, y_pred_nb, average='macro')

print('Naive Bayes Accuracy           :', round(acc_nb, 4))
print('Naive Bayes Macro F1           :', round(f1_nb, 4))

results.append(['Multinomial Naive Bayes', acc_nb, f1_nb])
trained_models['Multinomial Naive Bayes'] = nb_model

# Support Vector Machine
svm_model = LinearSVC(class_weight='balanced', random_state=42)
svm_model.fit(X_train_tfidf, y_train)

y_pred_svm = svm_model.predict(X_test_tfidf)
acc_svm = accuracy_score(y_test, y_pred_svm)
f1_svm = f1_score(y_test, y_pred_svm, average='macro')

print('SVM Accuracy                   :', round(acc_svm, 4))
print('SVM Macro F1                   :', round(f1_svm, 4))

results.append(['Support Vector Machine', acc_svm, f1_svm])
trained_models['Support Vector Machine'] = svm_model

# Random Forest
rf_model = RandomForestClassifier(
    n_estimators=200,
    class_weight='balanced',
    random_state=42,
    n_jobs=-1
)
rf_model.fit(X_train_tfidf, y_train)

y_pred_rf = rf_model.predict(X_test_tfidf)
acc_rf = accuracy_score(y_test, y_pred_rf)
f1_rf = f1_score(y_test, y_pred_rf, average='macro')

print('Random Forest Accuracy         :', round(acc_rf, 4))
print('Random Forest Macro F1         :', round(f1_rf, 4))

results.append(['Random Forest', acc_rf, f1_rf])
trained_models['Random Forest'] = rf_model

# XGBoost
xgb_model = XGBClassifier(
    objective='multi:softprob',
    num_class=len(np.unique(y_train)),
    n_estimators=200,
    max_depth=6,
    learning_rate=0.1,
    subsample=0.9,
    colsample_bytree=0.9,
    eval_metric='mlogloss',
    random_state=42,
    n_jobs=-1
)

xgb_model.fit(X_train_tfidf, y_train, sample_weight=sample_weights)

y_pred_xgb = xgb_model.predict(X_test_tfidf)
acc_xgb = accuracy_score(y_test, y_pred_xgb)
f1_xgb = f1_score(y_test, y_pred_xgb, average='macro')

print('XGBoost Accuracy               :', round(acc_xgb, 4))
print('XGBoost Macro F1               :', round(f1_xgb, 4))

results.append(['XGBoost', acc_xgb, f1_xgb])
trained_models['XGBoost'] = xgb_model

"""## Model Comparison"""

results_df = pd.DataFrame(results, columns=['Model', 'Accuracy', 'Macro_F1'])
results_df = results_df.sort_values(by=['Macro_F1', 'Accuracy'], ascending=False).reset_index(drop=True)
results_df

best_model_name = results_df.iloc[0]['Model']
best_model = trained_models[best_model_name]

print('Best Model:', best_model_name)
print('Reason: Macro F1-score is more important here because the dataset is imbalanced.')

# Classification report of best model
best_predictions = best_model.predict(X_test_tfidf)
print(classification_report(y_test, best_predictions, target_names=label_encoder.classes_))

